	<h2>Appearance</h2>

  <p> 
    Throughout the earlier years, Mickey's design bore heavy resemblance to Oswald, save for the ears, nose, and tail.[12][13][14] Ub Iwerks designed Mickey's body out of circles in order to make the character simple to animate. Disney employees John Hench and Marc Davis believed that this design was part of Mickey's success as it made him more dynamic and appealing to audiences.
    Mickey's circular design is most noticeable in his ears. In animation in the 1940s, Mickey's ears were animated in a more realistic perspective. Later, they were drawn to always appear circular no matter which way Mickey was facing. This made Mickey easily recognizable to audiences and made his ears an unofficial personal trademark. The circular rule later created a dilemma for toy creators who had to recreate a three-dimensional Mickey.
    Besides Mickey's gloves and shoes, he typically wears only a pair of shorts with two large buttons in the front. Before Mickey was seen regularly in color animation, Mickey's shorts were either red or a dull blue-green. With the advent of Mickey's color films, the shorts were always red. When Mickey is not wearing his red shorts, he is often still wearing red clothing such as a red bandmaster coat
  </p>

   

